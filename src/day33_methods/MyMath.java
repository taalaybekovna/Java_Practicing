package day33_methods;

public class MyMath {

    public static void Add(double One, double Two){
        System.out.println(One + " + " + Two + " = " + (One+Two));
    }

    public static void Plus(int Three, int Four){
        System.out.println(Three + " + " + Four + " = " + (Three+Four));
    }

    public static void main(String[] args) {

        Add(5.1, 6.1);
        Add(100.9, 300.8);
        Add(8.1, 8.7);
        Plus(5,6);
        Plus(1990, 1987);




    }
}
